package com.ngen.day1;

public class IterateExample {

	public static void main(String[] args) {
		
	//Using a for loop to print numbers from 1 through 10
		for(int i = 1; i <= 10; i++){
			System.out.println(i);
		}
		//using  a for loop to display numbers 1o to 1
		for (int k = 10; k >= 1; k--)
		
		//Using while loop to print numbers from 1 through 10
		int j = 1;
		while(j <= 10){
			System.out.println(j);
			j++;
		}
		
	}
	

}
